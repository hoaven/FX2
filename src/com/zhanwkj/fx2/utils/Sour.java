package com.zhanwkj.fx2.utils;

/**
 * MyBatis工具类
 * @author Chitry
 *
 */
public class Sour {

	private String key;
	private String value;

	public Sour() {
		super();
	}

	public Sour(String key, String value) {
		super();
		this.key = key;
		this.value = value;
	}

	@Override
	public String toString() {
		return "Sour [key=" + key + ", value=" + value + "]";
	}

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

}
