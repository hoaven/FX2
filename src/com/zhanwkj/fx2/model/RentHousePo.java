package com.zhanwkj.fx2.model;

import java.io.Serializable;

public class RentHousePo
  implements Serializable
{
  private static final long serialVersionUID = 1L;
  private RentHouse data;
  private String url;

  public RentHousePo(RentHouse data, String url)
  {
    this.data = data;
    this.url = url;
  }

  public RentHousePo()
  {
  }

  public RentHouse getData() {
    return this.data;
  }
  public void setData(RentHouse data) {
    this.data = data;
  }
  public String getUrl() {
    return this.url;
  }
  public void setUrl(String url) {
    this.url = url;
  }
}